package typecasting;

public class implicittypecasting {
    public static void main(String[] args) {
        int a=10;
        double d=a;
        System.out.println(a);
        System.out.println(d);
    }
    
}
//Implicit typecasting done automatically. conversion of smaller data type to large data type.
//syntax:-   datatype variable1=(datatypr)variable 2;
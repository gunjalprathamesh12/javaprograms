package typecasting;
public class implicit {
    public static void main(String[] args) {
        byte b=12;
        short s=b;
        int i=10;
        long l=b;
        double e=l;
        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
        System.out.println(e);
    }
    
}
